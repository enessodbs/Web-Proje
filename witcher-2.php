<h2 id="Gameplay" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Gameplay</h2><hr>
<ul><h3>According to previews:</h3></ul>
<li>The game features a few timed conversations, with a default response being used if players do not choose an option within a certain length of time. Players are not presented with the full, exact line that Geralt will deliver, instead choosing from among short summaries.</li><br>
<li>The combat system has been reworked, being "much more open-ended" and which "no longer requires the timed input of the original game".</li><br>
<li>The "romance cards" are not returning, although Geralt still has opportunities for encounters. These are now handled with cutscenes.</li><br>
<li>According to a Polish preview, it is possible to import a saved game from The Witcher.</li><br>
<li>Fighting alongside companions has received greater emphasis, although they still act autonomously rather than being under the control of the player. How players relate to these companions (both in conversations and elsewhere) can open up quests and affect the game's ending.</li><br>
<li>Meditation can now be done anywhere, provided there are no enemies nearby to prevent it, but Geralt must now enter meditation mode to drink a potion.</li><br>
<li>Geralt can now climb over walls and other such obstacles.</li>
