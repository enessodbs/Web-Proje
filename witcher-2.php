<p>The narrative takes Geralt from Foltest's side, through an assault on La Valette Castle, then through Flotsam, Aedirn (either the dwarven town of Vergen or a Kaedweni camp depending on his alliances) and finally to Loc Muinne where he has a final face off with a dragon.</p>
