<h2 id="aptr" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Announcements prior to release</h2><hr>
<p>A video showing Assassins of Kings was leaked on 16 September 2009, before the game was officially announced. According to CD PROJEKT RED, the video was intended for potential publishers, and had been created about six months before it was leaked. It was made using a "very early alpha version" of the game, and voices were provided by CD PROJEKT RED staff members rather than the final actors. CD PROJEKT RED published a short FAQ in response to the leak, but said that apart from that, they did "not plan any materials to be released soon". The game itself was said to be "still deeply in the development stage", and people should "not expect the game too soon".</p>
<br>
<p>The game was announced officially on 24 March 2010, with a number of articles and videos appearing. A two-minute trailer was released on GameTrailers, and articles (with accompanying video and screenshots) have been released in English, Polish, and Russian.</p>
<p>The official CDPR forum was launched on March 23, 2010 along with the official trailer. An official website followed in April.</p>
<br>

    <h2 id="Platforms" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Platforms</h2><hr>
<p>The PC version was released on May 17, 2011. The Xbox 360 version was released on April 17, 2012.</p><br>
<h3>Xbox 360 Version</h3>
<p>The Xbox 360 version offers several different features. These include Dark Mode, an extreme difficulty setting, Arena Mode, where players fight against unending waves of enemies, and content from all nine previously released DLC packs. This version also features new locations, a new target locking system, a better camera, better controls, two new story adventures totaling over four hours of gameplay, and the introduction of three new important characters. The additional in-game content is available in the PC version as part of the Enhanced Edition Patch.</p><p>Each of these stances has its own unique combat style. Both of Geralt's main swords, steel and silver, have distinctively different combat styles from the rest of his weaponry, and serve very distinct purposes: where the steel blade is used to fight humans and other flesh-and-blood beings, the silver is more effective against supernatural monsters and beasts (some of which may be completely unaffected by steel).</p>
<p>If the Dark Version is purchased, included with the game is a Making Of DVD, the game's soundtrack on CD, a world map, a Witcher medallion, a quest handbook, the game manual, an art book, stickers and a special box to hold everything in. The Enhanced Edition includes the game, the soundtrack, the world map, the quest handbook, and the manual.</p>
<p style="font-size: medium;">The Linux version was released on May 22, 2014.</p>

<h2 id="Story" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Story</h2><hr>
<img src="img/dw.jpg" style="height: 220px; ">
<p>The plot of The Witcher 2 is intended to be less linear than that of the first game, and is supposed to feature "three independent plot lines" which lead to multiple endings.[1]</p>
<p>The Witcher 2 is set almost immediately after the first, and follows from the attempt on King Foltest's life which occurred in the final cutscene[1]. Geralt will once again face the mysterious witcher-like assassins, who appear to be connected with a character named in previews as the Kingslayer.[2] In a trailer, the Kingslayer is seen speaking to Iorveth, a human-hating elf. The Kingslayer displays a head which he claims belonged to King Demavend of Aedirn, and says that if Iorveth is prepared to help, more kings will also fall.</p>
<p>Other plot elements which have been referenced (but which do not necessarily correspond to the three plot lines mentioned) are Foltest's moves to capture the stronghold of the rebellious Baroness La Valette and "Geralt's personal theme, in which he's looking for his own identity".[1]</p>
<p>According to a fact sheet released for the game, Geralt's travels "will lead him up the river Pontar into the turbulent borderland between Temeria, Kaedwen and Aedirn".[1] A town named Flotsam, situated on the border between Temeria and Aedirn, will feature.</p>
