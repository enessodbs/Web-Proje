<h3>Early demonstrated segment</h3>
<p>A small segment of the game's early stages was covered in some detail by previews and videos,[2] apparently having been used as a demonstration for the media.</p>
<p>The segment begins with Geralt, Triss, and Roche on a ship, on the Pontar river. They disembark and head into a forest, with Triss and Roche arguing. They are interrupted by the sound of someone playing a flute, and notice Iorveth watching them. Roche and Iorveth know each other, at least by reputation, and are hostile. A fight begins, with Iorveth signalling archers to attack — Triss is able to establish a shield to protect them (also seen in the trailer), but is weakened by the effort, and needs to be carried by Roche. The shield renders Iorveth's archers useless, but enemies can still pass through the shield on foot, and Geralt must hold them off alone while the three slowly make their way to the safety of the nearby village (Flotsam).</p>
<p>Later, the three are admitted to the town of Flotsam, where they find a public hanging in progress. Two of the four people waiting on the gallows are Dandelion and Zoltan, the latter of whom turns out to be an informant that Roche was looking for. Geralt moves to save them, arguing with the guards (with some support from the townspeople) and then fighting them. The town's leader (Bernard Loredo) then arrives and speaks with Geralt — the two unnamed prisoners are hanged, but Dandelion and Zoltan are spared, with the town official telling Geralt to speak with him later to hear a proposal.</p>
<p>Geralt can later visit a nearby elven ruin. This involves fighting their way through a stretch of forest, and then fighting looters at the ruins. Triss can accompany him, and when they find themselves trapped after a fall, there is an opportunity for a love scene (the one depicted in the trailer).</p>



