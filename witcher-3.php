<strong>The Witcher 3: Wild Hunt</strong><img src="img/TheWitcher3BoxArt.jpg" style="width: 260px; height: 360px;"> <p>The Witcher 3: Wild Hunt is the third and final installment in the series of games developed by CD PROJEKT RED featuring the witcher Geralt of Rivia. The game was originally scheduled for release in late 2014, then pushed back to 24 February 2015, and finally released on 19 May 2015.[3] During the first two weeks since release it had sold more than 4 million copies worldwide,[4] more than doubling the total sales of its predecessor, The Witcher 2: Assassins of Kings.[5]</p>
<p>Unlike the previous games, The Witcher 3 is set in a multi-region open world, featuring over 100 hours of content.[6] Several early articles mentioned that the developers planned to have an open world similar to that of Skyrim, but 20% larger.[7][8] The developers were quick to point out that while there was much to appreciate in Bethesda's open world concept, they were also making an effort not to repeat what they perceived as the flaws in-game. As with earlier games, the consequences of Geralt's actions will impact how the narrative progresses. The game has been built using the new REDengine 3 and was designed with player feedback from fans of previous Witcher games in mind.[9]</p>
<p>For fans of the series, the online event "Killing Monsters", a weekly giveaway of wallpapers and audio files that ran from 10 June 2013 to 14 August 2013, culminated in the release of the "Killing Monsters" cinematic trailer.</p>
<p>On 14 May 2015, CD PROJEKT RED (working with Digic Pictures) released a pre-launch short story trailer titled "A Night to Remember".</p>
<p>On September 4, 2020, it was announced that the game and its expansions would be getting a next-generation upgrade and would be released as a standalone purchase for the next-generation consoles (Xbox Series X and PlayStation 5) as well as PC. A free update will be given out to those who already own the game on the PC, Xbox One, and PlayStation 4.[1] While it was originally expected to be released in 2021[2] it was later announced that the next-generation update had been delayed to 2022[10] before CDPR announced it'd be released on December 14, 2022.[11]</p>
