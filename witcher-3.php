<h2 id="Story" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Story</h2><hr>
<p>The world is in chaos. The air is thick with tension and the smoke of burnt villages. The fearsome Empire of Nilfgaard has struck again, ravaging the helpless Northern Kingdoms. The once mighty who tried to use Geralt for their own gain are now gone. In these uncertain times, no one can say what fortune holds in store, who will bring peace to the world and who will cause it only misery. But a force darker and deadlier emerges.</p><br>
<p>The petty men and women commanding tin-plated armies fail to understand that their conflict is child's play compared to the Wild Hunt, the otherworldly threat which now looms. These ghastly spectral riders have for ages plagued humankind, bringing misery to the world. This time the Wild Hunt seeks one person in particular: the one individual Destiny itself bestowed upon Geralt, the one soul Geralt considers kin.</p>
<br>

    <h2 id="Features" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Features</h2><hr>
<p>The PC version was released on May 17, 2011. The Xbox 360 version was released on April 17, 2012.</p><br>


<ul><h3>General</h3></ul>
<li>A standalone adventure easily accessible to new players</li><br>
<li>Meaningful choices with consequences that change the story and the game world</li><br>
<li>Quests and main story threads that can be resolved in any order or completed in parallel</li><br>
<li>Unforgettable quests deeply linked with the core storyline and designed with care to draw players in</li><br>
<li>A breathtaking cinematic introduction illustrating the game background</li><br>
<li>Unique atmosphere, memorable characters, and gritty dialogue</li><br>
<br>

<ul><h3>Open world</h3></ul>
<li>A vast open world - 35 times larger than that in The Witcher 2</li><br>
<li>Multiple ecosystems and cultures that players can move between at will</li><br>
<li>Regions with different inspirations and deep cultural references, each with a distinct feel</li><br>
<li>Unforgettable quests deeply linked with the core storyline and designed with care to draw players in</li><br>
<li>Free exploration unspoiled by loading times</li><br>


<ul><h3>Living world</h3></ul>
<li>A world shaped by the player's decisions, but which, left to itself, goes on living</li><br>
<li>A realistic day and night cycle</li><br>
<li>A dynamic weather system</li><br>
<li>A living economy: The new in-game economy system varies the price of goods based on surrounding conditions or their place of origin relative to Geralt's current whereabouts in the world: the price of fish might differ depending on distance from water, and a village lying on a trade route for trappers and hunters might have many tanneries and leather workers, affecting the price of crafting components and armor.</li><br>
<li>A breathtaking cinematic introduction illustrating the game background</li><br>
<li>A unique, deep, consistent and expansive game world based on that described by Andrzej Sapkowski</li><br>


<ul><h3>Combat</h3></ul>
<li>Combat complexity based on a variety of actions at the player's disposal, not on correct attack sequencing</li><br>
<li>A Witcher Senses system bringing new tactical depth to combat</li><br>
<li>Monsters, each with their own lore, posing unique hunting challenges and responding to changes in the environment</li><br>


<ul><h3>RPG</h3></ul>
<li>A tutorial built into the game, allowing players to dive into the world of the Witcher with ease</li><br>
<li>Involving investigations and rewarding hunts through the Witcher Senses mechanic</li><br>
<li>A completely rewritten RPG mechanic allowing players to see the influence of statistics on gameplay</li><br>
<li>An expanded Sign casting system: each of the 5 Signs has two different modes of use, creating even more possibilities in combat and challenging players to use magic in creative ways</li><br>
<li>A highly-intuitive Alchemy system</li><br>
<li>An expanded character development system</li><br>
<li>Advanced Crafting mechanics</li><br>    


<ul><h3>Visuals</h3></ul>
<li>Fully dynamic light and shadow effects throughout the game world, generating superior atmosphere and ambiance</li><br>
<li>Detailed, realistic locations through a significant increase in polygon numbers</li><br>
<li>A deep role-playing experience through visually attractive, film-like presentation</li><br>
 

