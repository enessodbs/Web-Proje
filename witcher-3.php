<h2 id="Expansions" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Expansions</h2><hr>
<img src="img/Expansion.jpg" style="width: 600px;height: 150px;margin-right: 150px;"><br><br><br><br><br><br><br><br><br>
<p>On April 7, 2015, CD PROJEKT RED announced two major expansions for The Witcher 3: Wild Hunt, entitled Hearts of Stone and Blood and Wine.[13] CD PROJEKT RED said that both expansion packs included new enemies, characters, and gear. Players would also be reunited with some "dearly missed" characters from the series. Hearts of Stone was released on October 13, 2015, and Blood and Wine on May 31, 2016, both are available on all three major platforms. Players can purchase both expansions at once by getting the Expansion Pass, now on sale through several retailers.</p>
<p>On August 30, 2016, a Game of the Year edition (for PC) and Complete edition (for consoles) was released, containing the base game, both expansions, and all downloadable content.</p>

<h3>Hearts of Stone expansion</h3>
<ul style="list-style-type: none;">
<li>Hearts of Stone is a major expansion that adds over ten hours of additional content. Geralt will adventure into the wilds of No Man's Land and the nooks and alleys of Oxenfurt, where he will try to complete a contract from the mysterious Man of Glass. Caught in a thick tangle of deceit, Geralt will need all his cunning and strength to solve the mystery and emerge unscathed.
</li>
</ul>

<h3>Blood and Wine expansion</h3>
<ul style="list-style-type: none;">
<li>Blood and Wine is a major expansion that adds over twenty hours of additional content. A tale that will introduce the all-new in-game region of Toussaint, Blood and Wine will take Geralt to a land untainted by war, where an atmosphere of carefree indulgence and knightly ritual masks an ancient and bloody secret.</li>
</li>
</ul>


