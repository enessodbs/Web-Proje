<h2 id="ConsoleVersion" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Console Version</h2><hr>
<p>The Witcher: Rise of the White Wolf was to be a console role-playing game to be released in Fall 2009 but the project has since been suspended. While it would feature the same story as The Witcher PC game, based on The Witcher series of the same name by Polish author Andrzej Sapkowski, it would also use a new engine, created in cooperation with Widescreen Games, and a new action-based combat system.</p>

