<h2 id="TechnicalInformation" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Technical Information</h2><hr>
<p>The Witcher is powered by a new version of the BioWare Aurora Engine, modified for a single-player experience. A number of changes have been introduced to the original BioWare Aurora Engine, as well as troubleshooting and modding information, also described in the The Witcher technical information article.</p>


