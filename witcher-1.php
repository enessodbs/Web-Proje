<h2 id="Storyline" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">StoryLine</h2><hr>
<p>The game is centred on Geralt the witcher, also known as "the White Wolf", who at the beginning of the game suffers from amnesia. He gradually learns that he was a very renowned witcher, and that he had friends and enemies almost everywhere, who remember him but whom he does not remember. Through the player's actions, he will redefine those relationships and choose his path in the political intrigue that surrounds him.</p>


<br>
<h3 style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Story Motivation</h3>
<p>Story Motivations
    The developers have often said that their main goal with the game's storyline was to give choices to the player where there was no clear good or evil, and where the player would have to choose the lesser of two (or more) evils.[1]
    
    Also, the story was designed so that the consequences of the player's choices are often not immediately visible, and may only become apparent a few chapters later. This was done to prevent the "save, choose, reload" type of gameplay that is often used in similar computer role-playing games.</p>
   

<br>
    <h2 id="Gameplay" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Gameplay</h2><hr>
    <p><img style="height: 160px;" src="img/download.jpg">There are three camera styles available when playing The Witcher. The game can be played from one of two top-down perspectives, in which case the mouse is used to control everything, or it can be played from an over-the-shoulder (OTS) view, which brings the player closer to the in-game combat, but limits visibility. In all three views the keyboard and mouse controls can be changed to be primarily mouse focused, or a combined keyboard-and-mouse approach.</p>
<p>The combat system in The Witcher represents a departure from most RPGs. Players choose one of three fighting styles. The quick style allows for faster, less-damaging attacks with a greater chance of hitting faster enemies; the heavy style deals more damage in exchange for a slow attack speed, and a lower chance to hit faster enemies; and the group style, which features sweeping attacks best used if the player is surrounded. The player can switch between the styles at any point.</p>
<p>Each of these stances has its own unique combat style. Both of Geralt's main swords, steel and silver, have distinctively different combat styles from the rest of his weaponry, and serve very distinct purposes: where the steel blade is used to fight humans and other flesh-and-blood beings, the silver is more effective against supernatural monsters and beasts (some of which may be completely unaffected by steel).</p>
<p>Alchemy is a major part of gameplay. The player can create potions that increase health or endurance regeneration, allow Geralt to see in the dark, or provide other beneficial effects. The recipes for these potions can be learned through scrolls, or by experimentation. Once the player creates an unknown potion he can choose to drink it, but if the potion is a failure it will poison the character. Each time Geralt drinks potions they increase the toxicity level of his body. This can be reduced by drinking a special potion or by meditating at an inn or fireplace.</p>
<p>In addition to potions, the player can also create oils and bombs, respectively used to augment the damage done by weapons, or as weapons in combat. Neither can be created until talent points have been allocated into the corresponding skills.</p>

