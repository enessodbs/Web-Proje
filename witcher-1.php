<h2 id="LocalizationVariations" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Localization Variations</h2><hr>
<p>All the female portrait cards shown after Geralt's "conquests" were retouched to a more modest standard for the US release version. The in-game Dryad was also reskinned so her hair covered more of her body in this release.</p>
<p>Colorful dialogue between characters was cut in all English releases, especially through shortening of dialogue. Lead Designer Michał Madej has disputed claims by fans that this was due to the sometimes crude language, but that the decision to edit down dialogue occurred because of production-related concerns in game development. Proof reader Martin Pagan noticed this shortened version during his work and writer Sande Chen confirmed that it was not due to censorship. Fans have theorized that it may have been done for voice acting cost savings, especially since much of the vulgar language has been retained. Such cost savings would normally occur during any shortening of dialogue, even in cases where no major crudity was involved.</p>

