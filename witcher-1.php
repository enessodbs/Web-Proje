    <div class="ana"><p>
<strong>The Witcher</strong><img src="img/The_Witcher_EU_box.jpg"> is a role-playing video game for the PC, developed by CD PROJEKT RED and published by Atari.
Based on The Witcher series of the same name by Polish author Andrzej Sapkowski, the game utilizes BioWare's proprietary Aurora Engine and was released in October 2007.
An enhanced edition of the game, containing among other things the editor and two new adventures: The Price of Neutrality premium module and Side Effects premium module, was released in September 2008.
In the first 10 months following its release, the game sold 800,000 copies worldwide.
A console version using an entirely new engine and combat system, titled The Witcher: Rise of the White Wolf, was planned for release in Fall 2009, but development was suspended indefinitely on May 6, 2009.</p>
<br>
<p>The Witcher follows the story of Geralt of Rivia, a witcher: a traveling monster hunter for hire, gifted with unnatural powers. Taking place in a fictional medieval world, the game implements detailed visuals. The natural light during various phases of the day is realistically altered, and the day and night transitions serve to enrich the game's ambiance. The weather can dynamically change from a light drizzle to a dark, stormy downpour accompanied by thunder and lightning, and the NPCs react to the rain by hiding under roofs trying to get out of the rain.</p>
