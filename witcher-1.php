<h2 id="Spin-Offs" style="font-family:'Gill Sans', 'Gill Sans MT', Calibri, 'Trebuchet MS', sans-serif">Spin-Offs</h2><hr>
<p>Several spin-off games have been created either by CD PROJEKT RED or on the license from CDP.</p>
<h3>Mobile Phone Game</h3>
<p>The Witcher: Crimson Trail (Polish: Wiedźmin: Krwawy Szlak), also known as The Witcher Mobile is a mobile phone action game created developed by Breakpoint on the license from CD PROJEKT RED. It features Geralt in his youth as a promising young student, has just completed his training to become an elite monster slayer, a witcher.</p>
<h3>Card Games</h3>
<p>Two card games based on CD PROJEKT RED's The Witcher computer game have been designed by Kuźnia Gier. One, Wiedźmin: Przygodowa Gra Karciana (The Witcher: Adventure Cardgame) has been published by Kuźnia Gier and is being sold in Polish gaming stores, while the other, Wiedźmin: Promocyjna Gra Karciana (The Witcher Promo Card Game) is a simpler game added to the collector's edition of The Witcher in some countries.</p>
<h3>Browser Game</h3>
<p>The Witcher: Versus is a Flash-based multiplayer fighting browser game, developed for CD PROJEKT RED by one2tribe and launched in 2008. The game has you create a character from one of three classes and challenge other players to deadly battles. When you first sign up for Versus, which is tied into your thewitcher.com forum account if you have one, you choose from one of three character classes: Witcher, Sorceress and Frightener.</p>

